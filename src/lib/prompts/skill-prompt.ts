export function getSkillGenerationPrompt(skillText: string, profileName: string): string {
  return `Ты — эксперт по составлению образовательных программ и описанию компетенций.

Контекст: Формируется профиль специалиста "${profileName}".

На основе формулировки компетенции ниже сгенерируй структурированный JSON со следующими полями:

1. "name" — краткое название скилла (2-4 слова, ёмко)
2. "summary" — описание скилла в 1-2 предложениях
3. "details" — массив из 5-10 тезисов о том, что конкретно нужно знать и уметь
4. "coreTechnologies" — массив из 5-10 ключевых технологий, инструментов, библиотек
5. "checkQuestions" — массив ровно из 10 вопросов для самопроверки (от простых к сложным)
6. "project" — объект проекта для практики:
   - "title" — название проекта
   - "description" — описание (2-3 предложения)
   - "requirements" — массив требований к проекту
   - "deliverables" — массив ожидаемых результатов
   - "focusSkills" — массив скиллов, которые тренируются

ФОРМУЛИРОВКА КОМПЕТЕНЦИИ:
"${skillText}"

ВАЖНО:
- Отвечай ТОЛЬКО валидным JSON без markdown-форматирования (без \`\`\`json)
- Все тексты на русском языке
- Массив checkQuestions должен содержать РОВНО 10 элементов
- Все массивы должны быть непустыми

Пример структуры ответа:
{
  "name": "Анализ данных",
  "summary": "Умение собирать, обрабатывать и интерпретировать данные для принятия решений.",
  "details": ["Понимание основ статистики", "..."],
  "coreTechnologies": ["Python", "Pandas", "..."],
  "checkQuestions": ["Что такое...?", "...", "...", "...", "...", "...", "...", "...", "...", "..."],
  "project": {
    "title": "Анализ продаж",
    "description": "Проект по анализу данных о продажах.",
    "requirements": ["Загрузка CSV", "..."],
    "deliverables": ["Jupyter notebook", "..."],
    "focusSkills": ["Pandas", "..."]
  }
}`;
}

